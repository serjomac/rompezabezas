        function start_puzzle(x){
            if (x == 4) {
                row = 2;
                col = 2;
            } else if( x == 6) {
                row = 3;
                col = 2;
            } else if (x == 8){
                row = 4;
                col = 2;
            } else if (x == 9){
                row = 3;
                col = 3;
            } else if (x == 10){
                row = 5;
                col = 2;
            }

            $('#puzzle_solved').hide();
            $('#source_image').snapPuzzle({
                rows: row, columns: col,
                pile: '#pile',
                containment: '#puzzle-containment',
                onComplete: function(){
                    $('#source_image').fadeOut(150).fadeIn();
                    $('#puzzle_solved').show();
                }
            });
        }

        

